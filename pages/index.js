import Head from "next/head";
import { useEffect, useState } from "react";

import { fonts } from "../theme";

export default function Home() {
  const [height, setHeight] = useState(0);
  const [heightActual, setHeightActual] = useState(0);
  const [slider, setSlider] = useState(0);
  useEffect(() => {
    let heightNav = 16 * 3;
    let heightDom = innerHeight;
    setHeight(heightDom - heightNav);
    let sliderActual = 0;
    setSlider(sliderActual);
  }, []);

  const changeScroll = () => {
    setTimeout(() => {
      let heightNav = 16 * 3;
      let heightDom = innerHeight;
      let heightAhora = heightDom - heightNav;

      let sliderActual = slider;

      let scroll = null;

      if (window.scrollY > heightActual) {
        sliderActual < 3
          ? (sliderActual = sliderActual + 1)
          : (sliderActual = 3);
        scroll = heightAhora * sliderActual;
        console.log(sliderActual);
      } else if (window.scrollY < heightActual) {
        sliderActual > 0
          ? (sliderActual = sliderActual - 1)
          : (sliderActual = 0);
        scroll = heightAhora * sliderActual;
        console.log(sliderActual);
      }

      window.scroll(0, scroll);

      setHeight(heightAhora);
      setHeightActual(scroll);
      setSlider(sliderActual);
    }, 600);
  };

  return (
    <div onWheel={changeScroll} onTouchMove={changeScroll}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div style={{ background: "#456", height: height + "px" }}>
        <h1>Esto</h1>
      </div>
      <div style={{ background: "#789", height: height + "px" }}>
        <h1>Es</h1>
      </div>
      <div style={{ background: "#abc", height: height + "px" }}>
        <h1>Una</h1>
      </div>
      <div style={{ background: "#def", height: height + "px" }}>
        <h1>Prueba</h1>
      </div>
      <style jsx>{`
        div > div {
          align-items: center;
          color: #fff;
          display: flex;
          justify-content: center;
        }
        div > div > h1 {
          ${fonts.text};
        }
      `}</style>
    </div>
  );
}
